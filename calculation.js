function add(num1 , num2){
    var add = num1 + num2;
    return add;
}
console.log("Addition",add(5,10));

function sub(num1 , num2){
    var sub = num1 - num2 ;
    return sub;
}
console.log("Subtraction",sub(10,5));

function multi(num1,num2){
    var multi = num1 * num2 ;
    return multi;
}
console.log("Multiplication",multi(5,10));

function div(num1,num2){
    var div = num1 / num2 ;
    return div;
}
console.log("Division",div(10,5));

function mod(num1,num2){
    var mod = num1 % num2;
    return mod;
}
console.log("Modulus",mod(3,6));


